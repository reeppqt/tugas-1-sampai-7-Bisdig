import sys

class DynamicStack:
    def __init__(self):
        self.stack = []

    def push(self, data):
        self.stack.append(data)
        print(f"âœ… Data '{data}' ditambahkan ke stack.\n")

    def pop(self):
        if not self.stack:
            print("âš ï¸ Stack kosong! Tidak bisa pop.\n")
        else:
            removed = self.stack.pop()
            print(f"ðŸ—‘ï¸ Data '{removed}' dihapus dari stack.\n")

    def tampilkan(self):
        if not self.stack:
            print("ðŸ“­ Stack kosong.\n")
        else:
            print("ðŸ“¦ Isi Stack (Top â†’ Bottom):")
            for item in reversed(self.stack):
                print(f"- {item}")
            print()

    def monitor_memori(self):
        jumlah = len(self.stack)
        total_bytes = sum(sys.getsizeof(item) for item in self.stack)
        print(f"ðŸ“Š Monitoring Memori:")
        print(f"- Jumlah elemen: {jumlah}")
        print(f"- Estimasi total ukuran data: {total_bytes} byte\n")


def main():
    stack = DynamicStack()

